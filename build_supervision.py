def student_card(s, delay=0, show_institution=False):
    delay_class = f" reveal-delay-{delay}" if delay else ""
    status_html = ""
    if s.get("status") == "completed":
        status_html = f'<div class="student-card__status mt-4"><span class="tag tag--primary">{s["status_label"]}</span></div>'
    elif s.get("status") == "progress":
        status_html = '<div class="student-card__status mt-4"><span class="tag tag--muted">In Progress</span></div>'
    inst_html = ""
    if show_institution and s.get("institution"):
        inst_html = f'<p class="student-card__institution">{s["institution"]}</p>'
    return f'''      <article class="student-card reveal{delay_class}">
        <div class="student-card__photo-wrap">
          <img src="{s['photo']}" alt="Photo of {s['name']}" class="student-card__photo" loading="lazy" />
        </div>
        <p class="student-card__degree">{s['degree']}</p>
        <h3 class="student-card__name">{s['name']}</h3>
        <p class="student-card__thesis"><em>{s['thesis']}</em></p>
        {inst_html}{status_html}
      </article>'''

